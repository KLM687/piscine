int	ft_verif_arg(int **data, int size)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < size)
		{
			if (data[i][j] != 0)
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
